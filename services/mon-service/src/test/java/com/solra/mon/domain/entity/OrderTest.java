package com.solra.mon.domain.entity;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

/**
 * Order 实体单元测试。
 * 验证订单状态机和金额计算。
 */
@DisplayName("Order — 订单实体测试")
class OrderTest {

    @Nested
    @DisplayName("构造函数")
    class Constructor {

        @Test
        @DisplayName("创建后状态为 PENDING")
        void initialStatusIsPending() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            assertEquals(Order.OrderStatus.PENDING, order.getStatus());
        }

        @Test
        @DisplayName("初始 totalAmount 为 0")
        void initialTotalAmountIsZero() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            assertEquals(0L, order.getTotalAmount());
        }

        @Test
        @DisplayName("初始 items 为空")
        void initialItemsEmpty() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            assertTrue(order.getItems().isEmpty());
        }

        @Test
        @DisplayName("orderId 自动生成且唯一")
        void orderIdAutoGenerated() {
            Order o1 = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            Order o2 = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            assertNotNull(o1.getOrderId());
            assertNotNull(o2.getOrderId());
            assertNotEquals(o1.getOrderId(), o2.getOrderId());
        }
    }

    @Nested
    @DisplayName("addItem() — 添加订单项")
    class AddItem {

        @Test
        @DisplayName("添加单个商品")
        void addSingleItem() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            order.addItem("item-1", "Sword", 1, 100L);

            assertEquals(1, order.getItems().size());
            assertEquals(100L, order.getTotalAmount());
        }

        @Test
        @DisplayName("添加多个商品")
        void addMultipleItems() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            order.addItem("item-1", "Sword", 1, 100L);
            order.addItem("item-2", "Shield", 2, 50L);

            assertEquals(2, order.getItems().size());
            assertEquals(200L, order.getTotalAmount()); // 100 + 50*2
        }

        @Test
        @DisplayName("同一商品多数量")
        void sameItemMultipleQuantity() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.DIAMOND);
            order.addItem("item-1", "Gems", 10, 5L);

            assertEquals(1, order.getItems().size());
            assertEquals(50L, order.getTotalAmount());
        }
    }

    @Nested
    @DisplayName("complete() — 完成订单")
    class Complete {

        @Test
        @DisplayName("PENDING → COMPLETED")
        void pendingToCompleted() {
            Order order = createOrder();
            order.complete("alipay", "txn-12345");

            assertEquals(Order.OrderStatus.COMPLETED, order.getStatus());
            assertEquals("alipay", order.getPaymentChannel());
            assertEquals("txn-12345", order.getTransactionId());
        }
    }

    @Nested
    @DisplayName("fail() — 订单失败")
    class Fail {

        @Test
        @DisplayName("PENDING → FAILED")
        void pendingToFailed() {
            Order order = createOrder();
            order.fail();

            assertEquals(Order.OrderStatus.FAILED, order.getStatus());
        }
    }

    @Nested
    @DisplayName("refund() — 退款")
    class Refund {

        @Test
        @DisplayName("COMPLETED → REFUNDED")
        void completedToRefunded() {
            Order order = createOrder();
            order.complete("wechat", "txn-001");
            order.refund();

            assertEquals(Order.OrderStatus.REFUNDED, order.getStatus());
        }

        @Test
        @DisplayName("PENDING 状态退款抛异常")
        void pendingRefundThrows() {
            Order order = createOrder();
            assertThrows(IllegalStateException.class, () -> order.refund());
        }

        @Test
        @DisplayName("FAILED 状态退款抛异常")
        void failedRefundThrows() {
            Order order = createOrder();
            order.fail();
            assertThrows(IllegalStateException.class, () -> order.refund());
        }

        @Test
        @DisplayName("REFUNDED 状态再次退款抛异常")
        void refundedRefundThrows() {
            Order order = createOrder();
            order.complete("wechat", "txn-001");
            order.refund();
            assertThrows(IllegalStateException.class, () -> order.refund());
        }
    }

    @Nested
    @DisplayName("OrderItem")
    class OrderItemTest {

        @Test
        @DisplayName("创建 OrderItem")
        void createOrderItem() {
            Order.OrderItem item = new Order.OrderItem("item-1", "Sword", 2, 100L);

            assertEquals("item-1", item.getProductId());
            assertEquals("Sword", item.getProductName());
            assertEquals(2, item.getQuantity());
            assertEquals(100L, item.getUnitPrice());
        }
    }

    @Nested
    @DisplayName("多币种订单")
    class MultiCurrency {

        @Test
        @DisplayName("GOLD 订单")
        void goldOrder() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
            assertEquals(VirtualWallet.CurrencyType.GOLD, order.getCurrency());
        }

        @Test
        @DisplayName("DIAMOND 订单")
        void diamondOrder() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.DIAMOND);
            assertEquals(VirtualWallet.CurrencyType.DIAMOND, order.getCurrency());
        }

        @Test
        @DisplayName("FAITH_POINT 订单")
        void faithPointOrder() {
            Order order = new Order("user-1", VirtualWallet.CurrencyType.FAITH_POINT);
            assertEquals(VirtualWallet.CurrencyType.FAITH_POINT, order.getCurrency());
        }
    }

    private Order createOrder() {
        Order order = new Order("user-1", VirtualWallet.CurrencyType.GOLD);
        order.addItem("item-1", "Test Item", 1, 100L);
        return order;
    }
}
