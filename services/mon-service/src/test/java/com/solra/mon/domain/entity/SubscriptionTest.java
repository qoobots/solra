package com.solra.mon.domain.entity;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

/**
 * Subscription 实体单元测试。
 * 验证订阅生命周期状态机 + 四档等级 + 升降级逻辑。
 */
@DisplayName("Subscription — 订阅实体测试")
class SubscriptionTest {

    private static final long DAY_MS = 86400000L;

    @Nested
    @DisplayName("构造函数")
    class Constructor {

        @Test
        @DisplayName("创建后状态为 ACTIVE")
        void initialStatusIsActive() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            assertEquals(Subscription.SubscriptionStatus.ACTIVE, sub.getStatus());
        }

        @Test
        @DisplayName("autoRenew 由参数决定")
        void autoRenewByParam() {
            Subscription sub1 = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            Subscription sub2 = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, false);

            assertTrue(sub1.isAutoRenew());
            assertFalse(sub2.isAutoRenew());
        }

        @Test
        @DisplayName("subscriptionId 自动生成")
        void subscriptionIdAutoGenerated() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            assertNotNull(sub.getSubscriptionId());
        }

        @Test
        @DisplayName("BillingCycle 正确存储")
        void billingCycleStored() {
            Subscription monthly = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            Subscription yearly = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.YEARLY, true);

            assertEquals(Subscription.BillingCycle.MONTHLY, monthly.getBillingCycle());
            assertEquals(Subscription.BillingCycle.YEARLY, yearly.getBillingCycle());
        }
    }

    @Nested
    @DisplayName("isActive() — 活跃判断")
    class IsActive {

        @Test
        @DisplayName("ACTIVE 返回 true")
        void activeReturnsTrue() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            assertTrue(sub.isActive());
        }

        @Test
        @DisplayName("GRACE_PERIOD 返回 true")
        void gracePeriodReturnsTrue() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.enterGracePeriod();
            assertTrue(sub.isActive());
        }

        @Test
        @DisplayName("EXPIRED 返回 false")
        void expiredReturnsFalse() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.expire();
            assertFalse(sub.isActive());
        }

        @Test
        @DisplayName("CANCELLED 返回 false")
        void cancelledReturnsFalse() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.cancel("user request");
            assertFalse(sub.isActive());
        }
    }

    @Nested
    @DisplayName("cancel() — 取消订阅")
    class Cancel {

        @Test
        @DisplayName("状态变为 CANCELLED")
        void becomesCancelled() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.cancel("no longer needed");
            assertEquals(Subscription.SubscriptionStatus.CANCELLED, sub.getStatus());
        }

        @Test
        @DisplayName("取消后 autoRenew 变为 false")
        void autoRenewBecomesFalse() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.cancel("budget");
            assertFalse(sub.isAutoRenew());
        }
    }

    @Nested
    @DisplayName("expire() — 过期")
    class Expire {

        @Test
        @DisplayName("ACTIVE → EXPIRED")
        void activeToExpired() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.expire();
            assertEquals(Subscription.SubscriptionStatus.EXPIRED, sub.getStatus());
        }

        @Test
        @DisplayName("非 ACTIVE 状态 expire 不生效")
        void nonActiveExpireNoEffect() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.cancel("test");
            sub.expire();
            assertEquals(Subscription.SubscriptionStatus.CANCELLED, sub.getStatus());
        }
    }

    @Nested
    @DisplayName("enterGracePeriod() — 进入宽限期")
    class EnterGracePeriod {

        @Test
        @DisplayName("ACTIVE → GRACE_PERIOD")
        void activeToGracePeriod() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.enterGracePeriod();
            assertEquals(Subscription.SubscriptionStatus.GRACE_PERIOD, sub.getStatus());
        }

        @Test
        @DisplayName("非 ACTIVE 状态 enterGracePeriod 不生效")
        void nonActiveGracePeriodNoEffect() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.expire();
            sub.enterGracePeriod();
            assertEquals(Subscription.SubscriptionStatus.EXPIRED, sub.getStatus());
        }
    }

    @Nested
    @DisplayName("四档订阅等级 — FREE/PLUS/PRO/ULTRA")
    class Tiers {

        @Test
        @DisplayName("FREE 等级")
        void freeTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.FREE,
                    Subscription.BillingCycle.MONTHLY,
                    System.currentTimeMillis() + DAY_MS, false);
            assertEquals(Subscription.SubscriptionTier.FREE, sub.getTier());
            assertEquals(0, Subscription.SubscriptionTier.FREE.level());
        }

        @Test
        @DisplayName("PLUS 等级")
        void plusTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY,
                    System.currentTimeMillis() + DAY_MS, true);
            assertEquals(Subscription.SubscriptionTier.PLUS, sub.getTier());
            assertEquals(1, Subscription.SubscriptionTier.PLUS.level());
        }

        @Test
        @DisplayName("PRO 等级")
        void proTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.PRO,
                    Subscription.BillingCycle.MONTHLY,
                    System.currentTimeMillis() + DAY_MS, true);
            assertEquals(Subscription.SubscriptionTier.PRO, sub.getTier());
            assertEquals(2, Subscription.SubscriptionTier.PRO.level());
        }

        @Test
        @DisplayName("ULTRA 等级")
        void ultraTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.ULTRA,
                    Subscription.BillingCycle.MONTHLY,
                    System.currentTimeMillis() + DAY_MS, true);
            assertEquals(Subscription.SubscriptionTier.ULTRA, sub.getTier());
            assertEquals(3, Subscription.SubscriptionTier.ULTRA.level());
        }

        @Test
        @DisplayName("atLeast — FREE < PLUS < PRO < ULTRA")
        void atLeastComparison() {
            assertTrue(Subscription.SubscriptionTier.ULTRA.atLeast(Subscription.SubscriptionTier.PLUS));
            assertTrue(Subscription.SubscriptionTier.PRO.atLeast(Subscription.SubscriptionTier.PLUS));
            assertTrue(Subscription.SubscriptionTier.PLUS.atLeast(Subscription.SubscriptionTier.FREE));
            assertFalse(Subscription.SubscriptionTier.FREE.atLeast(Subscription.SubscriptionTier.PLUS));
            assertFalse(Subscription.SubscriptionTier.PLUS.atLeast(Subscription.SubscriptionTier.PRO));
        }
    }

    @Nested
    @DisplayName("升降级 — upgrade / downgrade")
    class UpgradeDowngrade {

        @Test
        @DisplayName("canUpgradeTo — PLUS→PRO 为 true")
        void canUpgradePlusToPro() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            assertTrue(sub.canUpgradeTo(Subscription.SubscriptionTier.PRO));
            assertTrue(sub.canUpgradeTo(Subscription.SubscriptionTier.ULTRA));
        }

        @Test
        @DisplayName("canUpgradeTo — PRO→PLUS 为 false")
        void cannotUpgradeProToPlus() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PRO,
                    Subscription.BillingCycle.MONTHLY, true);
            assertFalse(sub.canUpgradeTo(Subscription.SubscriptionTier.PLUS));
        }

        @Test
        @DisplayName("canDowngradeTo — PRO→PLUS 为 true")
        void canDowngradeProToPlus() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PRO,
                    Subscription.BillingCycle.MONTHLY, true);
            assertTrue(sub.canDowngradeTo(Subscription.SubscriptionTier.PLUS));
        }

        @Test
        @DisplayName("canDowngradeTo — 不能降到 FREE")
        void cannotDowngradeToFree() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            assertFalse(sub.canDowngradeTo(Subscription.SubscriptionTier.FREE));
        }

        @Test
        @DisplayName("upgrade — 等级和到期时间更新")
        void upgradeChangesTierAndExpire() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            long newExpire = System.currentTimeMillis() + 30 * DAY_MS;

            sub.upgrade(Subscription.SubscriptionTier.PRO, newExpire);

            assertEquals(Subscription.SubscriptionTier.PRO, sub.getTier());
            assertEquals(newExpire, sub.getExpireAt());
        }

        @Test
        @DisplayName("upgrade — 非法升级抛出异常")
        void upgradeToLowerThrows() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PRO,
                    Subscription.BillingCycle.MONTHLY, true);
            long newExpire = System.currentTimeMillis() + 30 * DAY_MS;

            assertThrows(IllegalArgumentException.class, () ->
                    sub.upgrade(Subscription.SubscriptionTier.PLUS, newExpire));
        }

        @Test
        @DisplayName("scheduleDowngrade — 降级成功")
        void scheduleDowngradeSuccess() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PRO,
                    Subscription.BillingCycle.MONTHLY, true);

            sub.scheduleDowngrade(Subscription.SubscriptionTier.PLUS);

            assertEquals(Subscription.SubscriptionTier.PLUS, sub.getTier());
        }

        @Test
        @DisplayName("scheduleDowngrade — 降级到 FREE 抛出异常")
        void downgradeToFreeThrows() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);

            assertThrows(IllegalArgumentException.class, () ->
                    sub.scheduleDowngrade(Subscription.SubscriptionTier.FREE));
        }
    }

    @Nested
    @DisplayName("renew — 续费")
    class Renew {

        @Test
        @DisplayName("续费延长到期时间")
        void renewExtendsExpire() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            long oldExpire = sub.getExpireAt();
            long newExpire = oldExpire + 30 * DAY_MS;

            sub.renew(newExpire);

            assertEquals(newExpire, sub.getExpireAt());
            assertTrue(sub.isAutoRenew());
        }

        @Test
        @DisplayName("已取消用户续费后状态变为 ACTIVE")
        void cancelledRenewBecomesActive() {
            Subscription sub = createSubscription(Subscription.SubscriptionTier.PLUS,
                    Subscription.BillingCycle.MONTHLY, true);
            sub.cancel("test");
            long newExpire = System.currentTimeMillis() + 30 * DAY_MS;

            sub.renew(newExpire);

            assertEquals(Subscription.SubscriptionStatus.ACTIVE, sub.getStatus());
            assertTrue(sub.isAutoRenew());
        }
    }

    @Nested
    @DisplayName("完整生命周期")
    class FullLifecycle {

        @Test
        @DisplayName("FREE→PLUS→PRO→降级→取消")
        void fullSubscriptionLifecycle() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.FREE,
                    Subscription.BillingCycle.MONTHLY,
                    System.currentTimeMillis() + 30 * DAY_MS, true);
            assertTrue(sub.isActive());

            // 升级到 PLUS
            sub.upgrade(Subscription.SubscriptionTier.PLUS,
                    System.currentTimeMillis() + 30 * DAY_MS);
            assertEquals(Subscription.SubscriptionTier.PLUS, sub.getTier());

            // 升级到 PRO
            sub.upgrade(Subscription.SubscriptionTier.PRO,
                    System.currentTimeMillis() + 30 * DAY_MS);
            assertEquals(Subscription.SubscriptionTier.PRO, sub.getTier());

            // 降级到 PLUS
            sub.scheduleDowngrade(Subscription.SubscriptionTier.PLUS);
            assertEquals(Subscription.SubscriptionTier.PLUS, sub.getTier());

            // 取消
            sub.cancel("不再需要");
            assertEquals(Subscription.SubscriptionStatus.CANCELLED, sub.getStatus());
            assertFalse(sub.isActive());
        }
    }

    private Subscription createSubscription(Subscription.SubscriptionTier tier,
                                             Subscription.BillingCycle cycle, boolean autoRenew) {
        return new Subscription("user-1", tier, cycle,
                System.currentTimeMillis() + DAY_MS * 30, autoRenew);
    }
}
