package com.solra.mon.domain.entity;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

/**
 * Subscription 实体单元测试。
 * 验证订阅生命周期状态机。
 */
@DisplayName("Subscription — 订阅实体测试")
class SubscriptionTest {

    @Nested
    @DisplayName("构造函数")
    class Constructor {

        @Test
        @DisplayName("创建后状态为 ACTIVE")
        void initialStatusIsActive() {
            Subscription sub = createSubscription(true);
            assertEquals(Subscription.SubscriptionStatus.ACTIVE, sub.getStatus());
        }

        @Test
        @DisplayName("autoRenew 由参数决定")
        void autoRenewByParam() {
            Subscription sub1 = createSubscription(true);
            Subscription sub2 = createSubscription(false);

            assertTrue(sub1.isAutoRenew());
            assertFalse(sub2.isAutoRenew());
        }

        @Test
        @DisplayName("subscriptionId 自动生成")
        void subscriptionIdAutoGenerated() {
            Subscription sub = createSubscription(true);
            assertNotNull(sub.getSubscriptionId());
        }
    }

    @Nested
    @DisplayName("isActive() — 活跃判断")
    class IsActive {

        @Test
        @DisplayName("ACTIVE 返回 true")
        void activeReturnsTrue() {
            Subscription sub = createSubscription(true);
            assertTrue(sub.isActive());
        }

        @Test
        @DisplayName("GRACE_PERIOD 返回 true")
        void gracePeriodReturnsTrue() {
            Subscription sub = createSubscription(true);
            sub.enterGracePeriod();
            assertTrue(sub.isActive());
        }

        @Test
        @DisplayName("EXPIRED 返回 false")
        void expiredReturnsFalse() {
            Subscription sub = createSubscription(true);
            sub.expire();
            assertFalse(sub.isActive());
        }

        @Test
        @DisplayName("CANCELLED 返回 false")
        void cancelledReturnsFalse() {
            Subscription sub = createSubscription(true);
            sub.cancel("user request");
            assertFalse(sub.isActive());
        }
    }

    @Nested
    @DisplayName("cancel() — 取消订阅")
    class Cancel {

        @Test
        @DisplayName("状态变为 CANCELLED")
        void becomesCancelled() {
            Subscription sub = createSubscription(true);
            sub.cancel("no longer needed");

            assertEquals(Subscription.SubscriptionStatus.CANCELLED, sub.getStatus());
        }

        @Test
        @DisplayName("取消后 autoRenew 变为 false")
        void autoRenewBecomesFalse() {
            Subscription sub = createSubscription(true);
            sub.cancel("budget");

            assertFalse(sub.isAutoRenew());
        }
    }

    @Nested
    @DisplayName("expire() — 过期")
    class Expire {

        @Test
        @DisplayName("ACTIVE → EXPIRED")
        void activeToExpired() {
            Subscription sub = createSubscription(true);
            sub.expire();

            assertEquals(Subscription.SubscriptionStatus.EXPIRED, sub.getStatus());
        }

        @Test
        @DisplayName("非 ACTIVE 状态 expire 不生效")
        void nonActiveExpireNoEffect() {
            Subscription sub = createSubscription(true);
            sub.cancel("test");
            sub.expire();

            assertEquals(Subscription.SubscriptionStatus.CANCELLED, sub.getStatus());
        }

        @Test
        @DisplayName("GRACE_PERIOD 状态 expire 不生效")
        void gracePeriodExpireNoEffect() {
            Subscription sub = createSubscription(true);
            sub.enterGracePeriod();
            sub.expire();

            assertEquals(Subscription.SubscriptionStatus.GRACE_PERIOD, sub.getStatus());
        }
    }

    @Nested
    @DisplayName("enterGracePeriod() — 进入宽限期")
    class EnterGracePeriod {

        @Test
        @DisplayName("ACTIVE → GRACE_PERIOD")
        void activeToGracePeriod() {
            Subscription sub = createSubscription(true);
            sub.enterGracePeriod();

            assertEquals(Subscription.SubscriptionStatus.GRACE_PERIOD, sub.getStatus());
        }

        @Test
        @DisplayName("非 ACTIVE 状态 enterGracePeriod 不生效")
        void nonActiveGracePeriodNoEffect() {
            Subscription sub = createSubscription(true);
            sub.expire();
            sub.enterGracePeriod();

            assertEquals(Subscription.SubscriptionStatus.EXPIRED, sub.getStatus());
        }
    }

    @Nested
    @DisplayName("订阅等级")
    class Tiers {

        @Test
        @DisplayName("FREE 等级")
        void freeTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.FREE,
                    System.currentTimeMillis() + 86400000L, false);
            assertEquals(Subscription.SubscriptionTier.FREE, sub.getTier());
        }

        @Test
        @DisplayName("PREMIUM 等级")
        void premiumTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.PREMIUM,
                    System.currentTimeMillis() + 86400000L, true);
            assertEquals(Subscription.SubscriptionTier.PREMIUM, sub.getTier());
        }

        @Test
        @DisplayName("CREATOR 等级")
        void creatorTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.CREATOR,
                    System.currentTimeMillis() + 86400000L, true);
            assertEquals(Subscription.SubscriptionTier.CREATOR, sub.getTier());
        }

        @Test
        @DisplayName("ENTERPRISE 等级")
        void enterpriseTier() {
            Subscription sub = new Subscription("user-1",
                    Subscription.SubscriptionTier.ENTERPRISE,
                    System.currentTimeMillis() + 86400000L, true);
            assertEquals(Subscription.SubscriptionTier.ENTERPRISE, sub.getTier());
        }
    }

    @Nested
    @DisplayName("完整生命周期")
    class FullLifecycle {

        @Test
        @DisplayName("ACTIVE → GRACE_PERIOD → EXPIRED")
        void activeGraceExpired() {
            Subscription sub = createSubscription(true);
            assertTrue(sub.isActive());

            sub.enterGracePeriod();
            assertEquals(Subscription.SubscriptionStatus.GRACE_PERIOD, sub.getStatus());
            assertTrue(sub.isActive());

            sub.expire();
            assertEquals(Subscription.SubscriptionStatus.GRACE_PERIOD, sub.getStatus());
            // expire 在 GRACE_PERIOD 状态不生效
        }

        @Test
        @DisplayName("ACTIVE → CANCELLED")
        void activeToCancelled() {
            Subscription sub = createSubscription(true);
            assertTrue(sub.isActive());

            sub.cancel("user request");
            assertEquals(Subscription.SubscriptionStatus.CANCELLED, sub.getStatus());
            assertFalse(sub.isActive());
        }
    }

    private Subscription createSubscription(boolean autoRenew) {
        return new Subscription("user-1", Subscription.SubscriptionTier.PREMIUM,
                System.currentTimeMillis() + 86400000L * 30, autoRenew);
    }
}
